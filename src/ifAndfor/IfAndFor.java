package ifAndfor;

public class IfAndFor {

	public static void main(String[] args) {
		
		//if statement
		int num =200;
		if(num <200) System.out.println("Num is less than 200...");
		
		if(num > 200) System.out.println("Num is greater than 200...");
		
		if(num == 200) System.out.println("Num is equal to  200...");
		
		
		//loop
		for(int n=0 ; n < 10; n++) {
			System.out.print(" "+n*n);
		}
	}

}
